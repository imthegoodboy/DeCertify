import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import "https://deno.land/x/xhr@0.1.0/mod.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const systemPrompt = `You are CertiBot, an AI assistant for DeCertify - a blockchain-based certificate verification platform built on Polygon Amoy Testnet.

Your knowledge:
- DeCertify allows organizations to issue blockchain-verified certificates
- Certificates are secured with certificate numbers and 6-digit PINs
- All certificates are recorded on Polygon blockchain for immutability
- Organizations register and receive a 6-digit verification code
- Users can verify certificates instantly using certificate number + PIN
- Certificates can be downloaded as professional PDFs with QR codes
- Platform uses Web3 wallet integration for secure certificate issuance

Your capabilities:
- Answer questions about how DeCertify works
- Explain blockchain certificate benefits
- Guide users through the issuance/verification process
- Provide information about Polygon blockchain and security
- Help troubleshoot common issues

Be helpful, concise, and professional. If asked about specific certificates, remind users to use the verification page with their certificate number and PIN.`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages,
        ],
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('AI API error:', response.status, errorText);
      throw new Error(`AI API error: ${response.status}`);
    }

    const data = await response.json();
    const aiResponse = data.choices[0]?.message?.content || 'I apologize, but I could not generate a response. Please try again.';

    return new Response(
      JSON.stringify({ response: aiResponse }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Error in chat-certibot function:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { 
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      }
    );
  }
});
